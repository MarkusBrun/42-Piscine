char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (i == 0)
		{
			str[i] += ('A' - 'a');
		}
		if (str[i] == ' ' || str[i] == '-' || str[i] == '+')
		{
			if ((str[i + 1] >= 'a' && str[i + 1] <= 'z'))
			{
				str[i + 1] -= ('a' - 'A');
			}
		}
		else if (str[i + 1] >= 'A' && str[i + 1] <= 'Z')
		{
			str[i + 1] -= ('A' - 'a');
		}
		i++;
	}
	return (str);
}

int	check(int n, int nb)
{
	if (n * n > nb)
	{
		return (0);
	}
	if (n * n == nb)
	{
		return (n);
	}
	else
	{
		return (check(n + 1, nb));
	}
}

int	ft_sqrt(int nb)
{
	int a;

	a = check(1, nb);
	return (a);
}
